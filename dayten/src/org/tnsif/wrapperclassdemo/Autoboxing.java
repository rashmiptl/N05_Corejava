package org.tnsif.wrapperclassdemo;
/*Program to demonstrate on Autoboxing
 * Autoboxing is the conversion of primitive type to object type/*/
public class Autoboxing {

	public static void main(String[] args) {
		char c='A';
		//Converting primitive data type into object type
		Character ch=c;
		System.out.println(ch);

	}

}
